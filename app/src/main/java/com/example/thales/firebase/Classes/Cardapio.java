package com.example.thales.firebase.Classes;

public class Cardapio {

    private String keyProduto;
    private String nomeUsuario;
    private String telefoneUsuario;
    private String emailUsuario;
    private String profissaoUsuario;
    private String urlImagem;


    public String getKeyProduto() {
        return keyProduto;
    }

    public void setKeyProduto(String keyProduto) {
        this.keyProduto = keyProduto;
    }

    public String getNomeUsuario() {
        return nomeUsuario;
    }

    public void setNomeUsuario(String nomeUsuario) {
        this.nomeUsuario = nomeUsuario;
    }

    public String getTelefoneUsuario() {
        return telefoneUsuario;
    }

    public void setTelefoneUsuario(String telefoneUsuario) {
        this.telefoneUsuario = telefoneUsuario;
    }

    public String getEmailUsuario() {
        return emailUsuario;
    }

    public void setEmaiUsuario(String emaiUsuario) {
        this.emailUsuario = emaiUsuario;
    }

    public String getProfissaoUsuario() {
        return profissaoUsuario;
    }

    public void setProfissaoUsuario(String profissaoUsuario) {
        this.profissaoUsuario = profissaoUsuario;
    }

    public String getUrlImagem() {
        return urlImagem;
    }

    public void setUrlImagem(String urlImagem) {
        this.urlImagem = urlImagem;
    }


}
